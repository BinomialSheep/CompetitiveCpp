struct Solver {
  void solve() {
    /* input */

    /* solve */
    INT(N, M);
    vector<vector<pair<int, pair<int, int>>>> G(N);
    // 重みなし無向グラフ
    rep(i, M) {
      INT(A, B, C, D);
      --A, --B;
      G[A].push_back(make_pair(B, make_pair(C, D)));
      G[B].push_back(make_pair(A, make_pair(C, D)));
    }

    auto f = [&](pair<int, int> p, ll d) {
      //
      ll ret = 1e18;
      ll tmp = sqrt(p.second);
      ll start = max(d, tmp - 10);
      for (ll i = start; i <= start + 10; i++) {
        chmin(ret, p.first + i + p.second / (i + 1));
      }
      return ret;
    };
    using pli = pair<ll, int>;

    priority_queue<pli, vector<pli>, greater<pli>> pq;
    pq.emplace(0, 0);
    ll INF = 1e18;
    vl dist(N, INF);
    dist[0] = 0;
    while (pq.size()) {
      auto [d, v] = pq.top();
      pq.pop();
      if (dist[v] < d) continue;
      for (auto [u, p] : G[v]) {
        ll d2 = f(p, d);
        // debug(p, d, f(p, d));
        if (chmin(dist[u], d2)) {
          pq.emplace(d2, u);
        }
      }
    }
    debug(dist);
    ll ans = dist[N - 1];
    if (ans == INF) ans = -1;
    out(ans);

    /* output */
  }
};

